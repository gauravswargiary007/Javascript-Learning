let array = [1, 34, 55, 3, 6, 88, 7, 98];
console.log(array);

// add element in the end push(value)
array.push(1111);
console.log("after array push() method: ", array);

// add element at the beginning unshift(value)
array.unshift(10022);
console.log("after array unshift() method: ", array);

// delete from the end pop()
array.pop();
console.log("after array pop() method: ", array);

// deletes the frist element shift()
array.shift();
console.log("after array shift() method: ", array);
